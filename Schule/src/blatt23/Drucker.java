package blatt23;

public class Drucker {
    private String verbindungsmodi;
    private String geraetemodus;
    private String abonnement;
    private String druckermodus;
    private String druckart;
    private String patronenart;


    public Drucker (String verbindungsmodi, String geraetemodus, String abonnement, String druckermodus, String druckart, String patronenart) {
        this.verbindungsmodi = verbindungsmodi;
        this.geraetemodus = geraetemodus;
        this.abonnement = abonnement;
        this.druckermodus = druckermodus;
        this.druckart = druckart;
        this.patronenart = patronenart;

    }
}
